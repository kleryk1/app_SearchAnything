	public static String[] concatWithArrayCopy( String[] main, String[] add ) {
		String[] ret_val = Arrays.copyOf( main, main.length + add.length );
	    System.arraycopy( add , 0, ret_val, main.length, add.length );
	    return ret_val;
	}
	
